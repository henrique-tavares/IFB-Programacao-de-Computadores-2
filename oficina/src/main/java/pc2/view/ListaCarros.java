    public void carregarCarro(boolean pesquisa) {
        // Pega o modelo da tabela de clientes
        DefaultTableModel modelo = (DefaultTableModel) tabCarros.getModel();
        // Limpar as linhas da tabela antes de preenchê-la
        modelo.setRowCount(0);
        // Desabilitar botões
        btnAlterar.setEnabled(false);
        btnExcluir.setEnabled(false);
        // Verifica se fez uma pesquisa
        if (pesquisa == false) {
            // Carregar a lista de clientes
            listaCarros = CarroDAO.listarTodos();
        }
        // Preenche a tabela de clientes
        for (Carro ca : listaCarros) {
            // Cria a linha da tabela
            Object[] linha = { ca.getMarca(), ca.getModelo(), ca.getAnoModelo(), ca.getAnoFabricacao(), ca.getPlaca(),
                    ClienteDAO.pesquisarCpf(ca.getCpfCliente()).getNome() };
            // adiciono a linha na tabela
            modelo.addRow(linha);
        }

    }

