    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnExcluirActionPerformed
        // Pega o número da linha selecionada pelo usuário
        int linha = tabCarros.getSelectedRow();
        if (linha == -1) {
            JOptionPane.showMessageDialog(null, "Selecione um carro para excluir!");
        } else {
            // Pega o cliente que está na linha selecionada
            Carro carro = listaCarros.get(linha);

            int confirmacao = JOptionPane.showConfirmDialog(null,
                    "Deseja realmente excluir o Carro " + carro.getPlaca() + "?", "Confirmação",
                    JOptionPane.YES_NO_OPTION, JOptionPane.ERROR_MESSAGE);

            if (confirmacao == JOptionPane.YES_OPTION) {
                if (CarroDAO.excluir(carro)) {
                    JOptionPane.showMessageDialog(null, "Cliente excluído com sucesso!");
                    carregarCarro();
                } else {
                    JOptionPane.showMessageDialog(null, "Erro ao excluir cliente!");
                }
            }

        }

    }// GEN-LAST:event_btnExcluirActionPerformed

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnAlterarActionPerformed
        int linha = tabCarros.getSelectedRow();
        if (linha > -1) {
            Carro ca = listaCarros.get(linha);

            CadastroCarros tela = new CadastroCarros(this);
            tela.setTitle("Alterar dados do carro");
            tela.setCliente(ca);
            tela.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um carro para alterar!");
        }
    }// GEN-LAST:event_btnAlterarActionPerformed

    private void tabCarrosMouseClicked(java.awt.event.MouseEvent evt) {// GEN-FIRST:event_tabCarrosMouseClicked
        // Verifica se há uma linha selecionada
        if (tabCarros.getSelectedRow() > -1) {
            // Habilita os botões
            btnAlterar.setEnabled(true);
            btnExcluir.setEnabled(true);
        }
    }// GEN-LAST:event_tabCarrosMouseClicked

    private void tabCarrosKeyPressed(java.awt.event.KeyEvent evt) {// GEN-FIRST:event_tabCarrosKeyPressed
        // Verifica se pressionou a tecla F4 do teclado
        if (evt.getKeyCode() == 115) {
            int linha = tabCarros.getSelectedRow();
            if (linha > -1) {
                Carro carro = listaCarros.get(linha);
            }
        }

        if (evt.getKeyCode() == 113) {
            int linha = tabCarros.getSelectedRow();
            if (linha > -1) {
                Carro carro = ListaCarros.get(linha);
                JOptionPane.showMessageDialog(null, carro);
            }
        }
    }// GEN-LAST:event_tabCarrosKeyPressed

    private void btnPequisarActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnPequisarActionPerformed
        listaCarros = CarroDAO.pesquisar(txtPesquisa.getText());
        carregarCarro(true);
    }// GEN-LAST:event_btnPequisarActionPerformed

    private void txtPesquisaKeyPressed(java.awt.event.KeyEvent evt) {// GEN-FIRST:event_txtPesquisaKeyPressed
        // Verifica se pressionou a tecla ENTER
        if (evt.getKeyCode() == 10) {
            listaCarros = CarroDAO.pesquisar(txtPesquisa.getText());
            carregarCarro(true);
        }

        // Verifica se pressionou a tecla ESC
        if (evt.getKeyCode() == 27) {
            txtPesquisa.setText("");
            carregarCarro();
        }
    }// GEN-LAST:event_txtPesquisaKeyPressed

